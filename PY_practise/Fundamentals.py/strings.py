#A String is a sequence of characters enclosed inside quotes.
# quotes "mad"

name = "MADHURI"
print(name[3]) #output: H
print(name[-1]) #output: I
print(name[0:3]) #output: MAD

#String Concatenation
#Concatenation means joining
first_name = "MADHURI"
last_name = "vinnakota"
print(first_name + " " + last_name) #output: MADHURI vinnakota


#string slicing
name = "MADHURI"
print(name[0:4]) #output:MADH
print(name[:4])  #MADH
print(name[3:])  #HURI
print(name[::-1]) #IRUHDAM


